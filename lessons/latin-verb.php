	<h1>Latin Verb Lesson</h1>

	<p>This lesson introduces the irregular verb <i>malo</i> (to prefer) to a class already familiar with the related verbs <i>volo</i> (to want) and <i>nolo</i> (not to want). It was originally given to a Latin III class of 13 students using Wheelock's Latin.</p>

	<p>In the lesson, the students review <i>volo</i> and <i>nolo</i> by conjugating the verbs from memory at the whiteboard. After the new forms of <i>malo</i> are introduced, students practice using the new verb in a spoken Latin activity where they ask their classmates about their preferences and record their findings, all in Latin. The worksheet provides scaffolding for their Latin conversation. After that, the students return to the whiteboard to play a game called Columns where they race as teams to conjugate the verb <i>malo</i> in all its forms. The exit ticket provides an opportunity to test their knowledge of <i>malo</i> individually.</p>

	<p>In this lesson, I aimed to combine my rigorous approach to grammar with the excitement of games and spoken Latin. Students were motivated to learn the verb forms to communicate with their classmates and compete in the game.</p>

