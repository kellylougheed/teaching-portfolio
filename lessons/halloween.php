	<h1>Math Halloween Game</h1>

	<p>This activity, designed for a 6th grade math class, guides students through coding a Halloween-themed game on Scratch or Snap! that reviews the coordinate plane and introduces variables through scorekeeping.</p>

	<p>The Halloween Game, which I designed, builds on student's knowledge of x- and y-coordinates by having them code a keyboard-controlled main character (witch) as well as obstacle and reward sprites that move randomly on the screen. Students also used positive and negative integers for scorekeeping, as the user could lose or gain points depending on which sprite they collided with.</p>

	<p>In their final products, students showcased their creativity by customizing the sprites and adding extra features, and they demonstrated a solid understanding of x- and y-coordinates.</p>

	<p>Halloween Game: move the witch with the keyboard keys! Try to collect pumpkins (+1 pt) and avoid the ghost (-1 pt).</p>

