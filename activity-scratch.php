	<h1>Scratch Activity</h1>

	<p>This activity is designed for coding students who have progressed at least to Unit 5 of the <a href="http://scratched.gse.harvard.edu/guide/files/CreativeComputing20141015.pdf" target="_blank">Scratch Curriculum Guide</a>. It was originally developed for a Computational Thinking &amp; Standards class at the College of St. Scholastica.</p>

	<p>The activity engages students by encouraging them to build with Scratch the type of personality quiz they may have encountered before online or in magazines. It reinforces preexisting knowledge of variables and conditionals and expands student knowledge of logical operators and nested conditionals.</p>

